# int - целое число
# float - дробное число
# if
# input() - это функция, позволяющая "забрать" данные с клавитуры
# функция - это какая-то программа (последовательность действий)
# почему после слова 'input' есть скобочки?
# скобочки дают нам понять, что перед нами функция
# НЕЛЬЗЯ называть переменные по названию функции, потому что функция перестанет работать и превратится в переменную

a = int(input())
b = float(input())
c = a + b
print(c)
